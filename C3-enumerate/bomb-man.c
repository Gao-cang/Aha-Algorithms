#include<stdio.h>
#include<stdlib.h>

int main(){
    char a[13][13]={
        {'#','#','#','#','#','#','#','#','#','#','#','#','#'},
        {'#','G','G','.','G','G','G','#','G','G','G','.','#'},
        {'#','#','#','.','#','G','#','G','#','G','#','G','#'},
        {'#','.','.','.','.','.','.','.','#','.','.','G','#'},
        {'#','G','#','.','#','#','#','.','#','G','#','G','#'},
        {'#','G','G','.','G','G','G','.','#','.','G','G','#'},
        {'#','G','#','.','#','G','#','.','#','.','#','#','#'},
        {'#','#','G','.','.','.','G','.','.','.','.','.','#'},
        {'#','G','#','.','#','G','#','#','#','.','#','G','#'},
        {'#','.','.','.','G','#','G','G','G','.','G','G','#'},
        {'#','G','#','.','#','G','#','G','#','.','#','G','#'},
        {'#','G','G','.','G','G','G','#','G','.','G','G','#'},
        {'#','#','#','#','#','#','#','#','#','#','#','#','#'},
    };
    int i,j,sum,map=0,p,q,x,y;

    for(i=0;i<=12;i++){
        for(j=0;j<=12;j++){//二重循环枚举地图上每一个点
            if(a[i][j]=='.'){
                sum=0;
                //站位向上统计
                x=i;y=j;
                while(a[x][y]!='#'){
                    if(a[x][y]=='G'){
                        sum++;
                    }
                    x--;
                }
                //站位向下统计
                x=i;y=j;
                while(a[x][y]!='#'){
                    if(a[x][y]=='G'){
                        sum++;
                    }
                    x++;
                }
                //站位向左统计
                x=i;y=j;
                while(a[x][y]!='#'){
                    if(a[x][y]=='G'){
                        sum++;
                    }
                    y--;
                }
                //站位向右统计
                x=i;y=j;
                while(a[x][y]!='#'){
                    if(a[x][y]=='G'){
                        sum++;
                    }
                    y++;
                }
                //记录最高杀敌数和站位的坐标
                if(sum>map){
                    map=sum;
                    p=i;
                    q=j;
                }
            }

        }
    }

    printf("When the bomb is setted on (%d,%d), it kills %d targets as best results.",p,q,map);
    system("pause");
    return 0;
}